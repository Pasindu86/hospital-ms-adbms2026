require('dotenv').config({ path: '../.env.local' });
const oracledb = require('oracledb');
const path = require('path');

async function run() {
    let connection;
    try {
        const walletDir = process.env.WALLET_DIR ? path.resolve(__dirname, '..', process.env.WALLET_DIR) : null;
        if (walletDir) {
            process.env.TNS_ADMIN = walletDir;
        }
        if (process.env.ORACLE_CLIENT_DIR) {
            oracledb.initOracleClient({ libDir: process.env.ORACLE_CLIENT_DIR });
        }

        connection = await oracledb.getConnection({
            user: process.env.DB_USER,
            password: process.env.DB_PASSWORD,
            connectString: process.env.DB_CONNECT_STRING,
            walletLocation: walletDir,
            walletPassword: process.env.WALLET_PASSWORD,
        });

        console.log('Connected to DB. Running setup...');

        const statements = [
            `BEGIN
         EXECUTE IMMEDIATE 'ALTER TABLE "ADMIN"."DOCTOR" ADD ("CONSULTATION_FEE" NUMBER DEFAULT 0)';
       EXCEPTION
         WHEN OTHERS THEN
           IF SQLCODE != -1430 THEN RAISE; END IF;
       END;`,

            `BEGIN
         EXECUTE IMMEDIATE 'CREATE TABLE "ADMIN"."BOOKING_PAYMENT" (    
            "PAYMENT_ID" NUMBER GENERATED BY DEFAULT AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER NOCYCLE NOKEEP NOSCALE, 
            "PATIENT_ID" NUMBER NOT NULL ENABLE, 
            "DOCTOR_ID" NUMBER NOT NULL ENABLE, 
            "PATIENT_NAME" VARCHAR2(100 BYTE) COLLATE "USING_NLS_COMP" NOT NULL ENABLE, 
            "DOCTOR_NAME" VARCHAR2(100 BYTE) COLLATE "USING_NLS_COMP" NOT NULL ENABLE, 
            "APPOINTMENT_DATE" TIMESTAMP (6) NOT NULL ENABLE, 
            "DOCTOR_FEE" NUMBER DEFAULT 0, 
            "HOSPITAL_FEE" NUMBER DEFAULT 500, 
            "TOTAL_AMOUNT" NUMBER DEFAULT 500, 
            "PAYMENT_STATUS" VARCHAR2(50 BYTE) COLLATE "USING_NLS_COMP" DEFAULT ''Paid'', 
            "PAYMENT_METHOD" VARCHAR2(50 BYTE) COLLATE "USING_NLS_COMP", 
            "TRANSACTION_DATE" TIMESTAMP (6) DEFAULT CURRENT_TIMESTAMP,
            CONSTRAINT "PK_BOOKING_PAYMENT" PRIMARY KEY ("PAYMENT_ID")
         )';
       EXCEPTION
         WHEN OTHERS THEN
           IF SQLCODE != -955 THEN RAISE; END IF;
       END;`,

            `CREATE OR REPLACE EDITIONABLE PROCEDURE "ADMIN"."SAVE_BOOKING_PAYMENT" (
            p_patient_id      IN NUMBER,
            p_doctor_id       IN NUMBER,
            p_patient_name    IN VARCHAR2,
            p_doctor_name     IN VARCHAR2,
            p_appointment_date IN TIMESTAMP,
            p_doctor_fee      IN NUMBER,
            p_hospital_fee    IN NUMBER,
            p_payment_method  IN VARCHAR2,
            p_payment_status  IN VARCHAR2,
            p_payment_id      OUT NUMBER
        ) AS
            v_total NUMBER;
        BEGIN
            v_total := COALESCE(p_doctor_fee, 0) + COALESCE(p_hospital_fee, 0);

            INSERT INTO BOOKING_PAYMENT (
                PATIENT_ID, DOCTOR_ID, PATIENT_NAME, DOCTOR_NAME, 
                APPOINTMENT_DATE, DOCTOR_FEE, HOSPITAL_FEE, TOTAL_AMOUNT, 
                PAYMENT_METHOD, PAYMENT_STATUS
            ) VALUES (
                p_patient_id, p_doctor_id, p_patient_name, p_doctor_name, 
                p_appointment_date, p_doctor_fee, p_hospital_fee, v_total, 
                p_payment_method, p_payment_status
            ) RETURNING PAYMENT_ID INTO p_payment_id;
        END;`
        ];

        for (let stmt of statements) {
            console.log('Executing:\\n', stmt.substring(0, 50) + '...');
            await connection.execute(stmt);
            console.log('Done.');
        }

        console.log('Setup finished successfully!');
    } catch (err) {
        console.error('Error setup DB:', err);
    } finally {
        if (connection) {
            try {
                await connection.close();
            } catch (err) {
                console.error(err);
            }
        }
    }
}

run();
