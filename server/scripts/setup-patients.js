const oracledb = require('oracledb')
const dotenv = require('dotenv')
const path = require('path')

dotenv.config({ path: path.join(__dirname, '..', '.env.local') })

const { DB_USER, DB_PASSWORD, DB_CONNECT_STRING, WALLET_DIR, WALLET_PASSWORD } = process.env

if (WALLET_DIR) {
  process.env.TNS_ADMIN = path.resolve(__dirname, '..', WALLET_DIR)
}

async function setup() {
  const poolConfig = {
    user: DB_USER,
    password: DB_PASSWORD,
    connectString: DB_CONNECT_STRING,
  }
  if (WALLET_DIR) poolConfig.walletLocation = process.env.TNS_ADMIN
  if (WALLET_PASSWORD) poolConfig.walletPassword = WALLET_PASSWORD

  await oracledb.createPool(poolConfig)
  const connection = await oracledb.getConnection()

  // 1. Drop existing tables if they exist
  try {
    await connection.execute(`DROP TABLE doctor_patient CASCADE CONSTRAINTS`)
    console.log('Dropped existing doctor_patient table.')
  } catch (e) {
    console.log('doctor_patient table did not exist or could not be dropped:', e.message)
  }

  try {
    await connection.execute(`DROP TABLE patients CASCADE CONSTRAINTS`)
    console.log('Dropped existing patients table.')
  } catch (e) {
    console.log('Patients table did not exist or could not be dropped:', e.message)
  }

  // 2. Create patients table
  try {
    await connection.execute(`
      CREATE TABLE patients (
        patient_id      NUMBER GENERATED BY DEFAULT AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER NOCYCLE NOKEEP NOSCALE PRIMARY KEY,
        name            VARCHAR2(100 BYTE),
        email           VARCHAR2(255 BYTE),
        phone_number    VARCHAR2(20 BYTE),
        address         VARCHAR2(500 BYTE),
        disease         VARCHAR2(255 BYTE),
        date_of_birth   DATE,
        gender          VARCHAR2(10 BYTE)
      )
    `)
    console.log('Created patients table.')
  } catch (e) {
    console.error('Error creating patients table:', e.message)
    throw e
  }

  // Create doctor_patient junction table
  try {
    await connection.execute(`
      CREATE TABLE doctor_patient (
        doctor_id   NUMBER NOT NULL,
        patient_id  NUMBER NOT NULL,
        CONSTRAINT pk_doctor_patient PRIMARY KEY (doctor_id, patient_id),
        CONSTRAINT fk_dp_doctor FOREIGN KEY (doctor_id) REFERENCES user_auth(user_id) ON DELETE CASCADE,
        CONSTRAINT fk_dp_patient FOREIGN KEY (patient_id) REFERENCES patients(patient_id) ON DELETE CASCADE
      )
    `)
    console.log('Created doctor_patient table.')
  } catch (e) {
    console.error('Error creating doctor_patient table:', e.message)
    throw e
  }

  // 3. Create PL/SQL procedure to insert/save a patient and assign a doctor
  try {
    const procedureDDL = `
      CREATE OR REPLACE PROCEDURE save_patient (
        p_patient_id     OUT NUMBER,
        p_name           IN VARCHAR2,
        p_email          IN VARCHAR2,
        p_phone_number   IN VARCHAR2,
        p_address        IN VARCHAR2,
        p_disease        IN VARCHAR2,
        p_dob            IN DATE,
        p_gender         IN VARCHAR2,
        p_doctor_id      IN NUMBER
      ) AS
        v_new_id NUMBER;
      BEGIN
        INSERT INTO patients (name, email, phone_number, address, disease, date_of_birth, gender)
        VALUES (p_name, p_email, p_phone_number, p_address, p_disease, p_dob, p_gender)
        RETURNING patient_id INTO v_new_id;

        IF p_doctor_id IS NOT NULL AND p_doctor_id > 0 THEN
          INSERT INTO doctor_patient (doctor_id, patient_id)
          VALUES (p_doctor_id, v_new_id);
        END IF;

        p_patient_id := v_new_id;
      END;
    `
    await connection.execute(procedureDDL)
    console.log('Created save_patient PL/SQL procedure successfully.')
  } catch (e) {
    console.error('Error creating PL/SQL procedure:', e.message)
    throw e
  }

  await connection.close()
  await oracledb.getPool().close(0)
}

setup().catch(console.error)
