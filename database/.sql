  CREATE OR REPLACE EDITIONABLE PROCEDURE "ADMIN"."SP_RESTOCK_MEDICINE" (p_drug_id IN NUMBER, p_amount IN NUMBER) IS
        v_capacity NUMBER;
        v_curr_stock NUMBER;
      BEGIN
        SELECT QUANTITY, CAPACITY INTO v_curr_stock, v_capacity FROM DRUG_STOCK WHERE DRUG_ID = p_drug_id;
