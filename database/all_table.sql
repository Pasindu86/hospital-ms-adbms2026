  CREATE OR REPLACE EDITIONABLE PROCEDURE "ADMIN"."SAVE_PATIENT" (
        p_patient_id     OUT NUMBER,
        p_name           IN VARCHAR2,
        p_email          IN VARCHAR2,
        p_phone_number   IN VARCHAR2,
        p_address        IN VARCHAR2,
        p_disease        IN VARCHAR2,
        p_dob            IN DATE,
        p_gender         IN VARCHAR2,
        p_doctor_id      IN NUMBER
      ) AS
        v_new_id NUMBER;
      BEGIN
        INSERT INTO patients (name, email, phone_number, address, disease, date_of_birth, gender)
        VALUES (p_name, p_email, p_phone_number, p_address, p_disease, p_dob, p_gender)
        RETURNING patient_id INTO v_new_id;

        IF p_doctor_id IS NOT NULL AND p_doctor_id > 0 THEN
          INSERT INTO doctor_patient (doctor_id, patient_id)
          VALUES (p_doctor_id, v_new_id);
        END IF;

        p_patient_id := v_new_id;
      END;



