--  DDL for Trigger TRG_DRUG_STOCK_DECREMENT
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE TRIGGER "ADMIN"."TRG_DRUG_STOCK_DECREMENT" 
AFTER INSERT ON "ADMIN"."PRESCRIPTION_ITEM"
FOR EACH ROW
BEGIN
    UPDATE DRUG_STOCK
    SET QUANTITY = QUANTITY - 1
    WHERE DRUG_ID = :NEW.DRUG_ID
      AND QUANTITY > 0;

    IF SQL%ROWCOUNT = 0 THEN
        RAISE_APPLICATION_ERROR(
            -20001,
            'Drug out of stock: DRUG_ID = ' || :NEW.DRUG_ID
        );
    END IF;
END;


